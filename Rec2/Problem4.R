library(MASS)
library(patchwork)
s1 = as.data.frame(mvrnorm(1000, c(2,3), matrix(c(1,0,0,1),ncol = 2)))
colnames(s1) = c("x1","x2")
s2 = as.data.frame(mvrnorm(1000, c(2,3), matrix(c(1,0,0,5),ncol = 2)))
colnames(s2) = c("x1","x2")
s3 = as.data.frame(mvrnorm(1000, c(2,3), matrix(c(1,2,2,5),ncol = 2)))
colnames(s3) = c("x1","x2")
s4 = as.data.frame(mvrnorm(1000, c(2,3), matrix(c(1,-2,-2,5),ncol = 2)))
colnames(s4) = c("x1","x2")

p1 = ggplot(s1, aes(x1,x2)) + geom_point()
p2 = ggplot(s2, aes(x1,x2)) + geom_point()
p3 = ggplot(s3, aes(x1,x2)) + geom_point()
p4 = ggplot(s4, aes(x1,x2)) + geom_point()
(p1+p2)/(p3+p4)
